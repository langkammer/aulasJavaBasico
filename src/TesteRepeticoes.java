public class TesteRepeticoes {

    public static void main(String[] args) {

        for (int numero = 1; numero <= 10; numero++) {
            System.out.println("Ola for numero " + numero);
        }

        int numero = 1;

        while (numero <= 10) {
            System.out.println("Ola while numero " + numero);
            numero++;
        }

    }
}
