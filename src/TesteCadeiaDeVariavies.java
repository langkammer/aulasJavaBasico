public class TesteCadeiaDeVariavies {

    public static void main(String[] args) {
        int numero = 50;
        int numeroDois = 25;

        int resultadoSoma = numero + numeroDois;

        int resultadoSubtiracao = numero - numeroDois;

        int resultadoMultiplicacao = numero * numeroDois;

        int resultadoDivisao = numero / numeroDois;


        System.out.println("Resultado da soma da Variavel 1 + Variavel 2 é : " + resultadoSoma);
        System.out.println("Resultado da subtração da Variavel 1 + Variavel 2 é : " + resultadoSubtiracao);
        System.out.println("Resultado da multiplicação da Variavel 1 + Variavel 2 é : " + resultadoMultiplicacao);
        System.out.println("Resultado da divisão da Variavel 1 + Variavel 2 é : " + resultadoDivisao);


    }

}
